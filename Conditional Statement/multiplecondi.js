let mode = "Dark";
let color;
if (mode === "Dark") {
    color = "black";
}
else if (mode === "light") {
    color = "white";
}
else if (mode === "blue") {
    color = "blue";
}
else if (mode === "green") {
    color = "green";
}
else {
    color = "yellow";
}
